print("Data[-1]:",Data[-1]);    #111
print("Data[-2]:",Data[-2]);    #101

print("Data[1:3]:",Data[1:3]);  #[21,51]
print("Data[0:4]:",Data[0:4]);  #[11,21,51,101]


